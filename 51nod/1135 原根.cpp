#include <stdio.h>
#include <math.h>
#include <string.h>
#include <iostream>
#include <algorithm>
using namespace std;
typedef long long  LL;
const int maxn = 1e6 + 10;
int prime[maxn];//存储素数
int sprime[maxn];//存储P-1的素因子
bool isp[maxn];//结果只有0和1，判断是否为素数
int k;//记录Maxn以内的素数个数
int cnt;//记录素因子的个数
void is_prime()
{
    memset(isp,true,sizeof(isp));//将所有的二进制数都标为1
    for (int i = 2; i<maxn; i++)
    {
        if (isp[i])
        {
            prime[k++] = i;
            for (int j = i + i; j<maxn; j += i)
                isp[j] = false;
        }
    }
}
void Divide(int n)//将n分解为素因子
{
    cnt = 0;
    int t = (int)sqrt(1.0*n);
    for (int i = 0; prime[i] <= t; i++)
    {
        if (n%prime[i] == 0)
        {
            sprime[cnt++] = prime[i];
            while (n%prime[i] == 0)//因为有可能有多个peime[i]
                n /= prime[i];
        }
    }
    if (n>1)
        sprime[cnt++] = n;//可能只有自己一个素因子
}
//求a^b%mod
LL modexp(LL a, LL b, int mod)//快速幂取模
{
    LL res = 1;
    while (b>0)
    {
        a = a%mod;
        if (b & 1)
            res = res*a%mod;
        b = b >> 1;
        a = a*a%mod;
    }
    return res;
}

int main()
{
    int p;
    is_prime();
    while (~scanf("%d", &p))
    {
        Divide(p - 1);
        for (int g = 2; g<p; g++)
        {
            int flag = 1;
            for (int i = 0; i<cnt; i++)
            {
                int t = (p - 1) / sprime[i];
                if (modexp(g, t, p) == 1)
                {
                    flag = 0;
                    break;
                }
            }
            if (flag)
            {
                int root = g;
                printf("%d\n", root);
                break;//去掉break的话是求所有的原根，加上break是求最小的原根、
            }
        }
    }
    return 0;
}